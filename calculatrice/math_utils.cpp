#include <iostream>
#include "math_utils.h"

int addition(int a, int b) {
    return a + b;
}

// Soustraction
int soustraction(int a, int b) {
    return a - b;
}

// Multiplication
int multiplication(int a, int b) {
    return a * b;
} 

// Division
double division(int a, int b) {
    return a / b;
}